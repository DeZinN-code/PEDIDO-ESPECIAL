let options = ""

do {
    options = prompt("Olá Baezinho, venho por meio desse programa fazer um pedido especial." +
    "\nVocê poderia sentar na minha cara e me deixar feliz para sempre?" +
    "\n1. sim" +
    "\n2. não").toLowerCase()

    if (options === "sim") {   
        alert("Obrigado por aceitar meu pedido! Você irá me fazer muito feliz amanhã, espero ficar com a cara na sua bunda para sempre, minha gostosa!")
    } else {
        alert("Você não aceitou meu pedido, tudo que eu queria era ficar chupando sua buceta gostosa. Mas tudo bem,eu te amo, mas estou triste.")
    }
} while (options !== "sim" && options !== "não")
